/**
 * Sơ đồ 3 khối
 * Input:
 * Lương 1 ngày: 100.000 (var salaryOneDay = 100000)
 * Biến day là số ngày làm người dùng nhập vào
 *
 * Process:
 * 1. Nhập vào số ngày làm day: var day = 24
 * 2. Tính tiền lương nhân viên, lương nhân viên bằng lương 1 ngày nhân với số ngày làm: var salary = salaryOneDay * day
 * console.log("Tiền lương nhân viên:", salary);
 *
 * Output
 * Tính tiền lương nhân viên (var salary = salaryOneDay * day)
 */
var salaryOneDay = 100000;
var day = 24;
var salary = salaryOneDay * day;
console.log("Tiền lương nhân viên:", salary);
